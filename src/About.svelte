  <h3>Fischer Imaging. A new company…</h3>
  <p>
    Founded in 2016, the new Fischer Imaging is focused on improving outcomes
    for cancer patients with advanced imaging. Our proprietary technology
    doubles the resolution and contrast of today’s mammography using less than
    half the x-ray dose and removes the need for painful breast compression.
  </p>

  <h3>With an impressive past.</h3>
  <p>
    Our company succeeds Fischer Imaging Corp., with roots going back to 1910
    when H.G. Fischer, the oldest manufacturer of X-ray imaging devices in the
    United States, began its operation in Chicago, IL. Since then, the
    reputation and name of the company have evolved and gained worldwide
    recognition in leveraging technology that has brought to market such
    innovations as the SenoScan Full Field Digital Mammography (FFDM) system and
    the MammoTest stereotactic breast biopsy system.
  </p>
